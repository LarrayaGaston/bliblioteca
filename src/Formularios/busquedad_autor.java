    private void bto_agregar_autorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bto_agregar_autorActionPerformed
        // TODO add your handling code here:
        Autor miAutor = new Autor();
        
        miAutor.setIdautor(Integer.parseInt(tabla_autor.getValueAt(tabla_autor.getSelectedRow(),0).toString())); 
        miAutor.setNombre_autor(tabla_autor.getValueAt(tabla_autor.getSelectedRow(),1).toString());
        miAutor.setApellido_autor(tabla_autor.getValueAt(tabla_autor.getSelectedRow(),2).toString());
        miAutor.setNacionalidad(tabla_autor.getValueAt(tabla_autor.getSelectedRow(),3).toString());
        
        DefaultListModel modelo_lista = (DefaultListModel)lista_autor_hijo.getModel();
       
       lista_autor_hijo.setModel(modelo_lista);
       modelo_lista.addElement(miAutor);
    }//GEN-LAST:event_bto_agregar_autorActionPerformed

